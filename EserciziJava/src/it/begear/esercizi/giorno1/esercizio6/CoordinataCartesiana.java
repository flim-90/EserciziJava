package it.begear.esercizi.giorno1.esercizio6;

public class CoordinataCartesiana {

	private double x;
	private double y;

	public CoordinataCartesiana(double x, double y) {
		super();
		setX(x);
		setY(y);
	}

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}

}
