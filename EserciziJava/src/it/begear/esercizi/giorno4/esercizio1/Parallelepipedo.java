package it.begear.esercizi.giorno4.esercizio1;

public class Parallelepipedo {

	private int base;
	private int altezza;
	public Parallelepipedo(int base, int altezza) {
		super();
		setBase(base);
		setAltezza(altezza);
	}
	public int getBase() {
		return base;
	}
	public void setBase(int base) {
		this.base = base;
	}
	public int getAltezza() {
		return altezza;
	}
	public void setAltezza(int altezza) {
		this.altezza = altezza;
	}
	
	
	
	
}
